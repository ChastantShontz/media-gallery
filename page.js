function haazenView() {
  var x = document.getElementById('haazen');
  x.classList.add('charTrans');
  var x = document.getElementById('haazenHid');
  x.classList.add('charHidTrans');
  var x = document.getElementById('layer');
  x.classList.add('layerTrans');
}

function haazenClose() {
  var x = document.getElementById('haazen');
  x.classList.remove('charTrans');
  var x = document.getElementById('haazenHid');
  x.classList.remove('charHidTrans');
  var x = document.getElementById('layer');
  x.classList.remove('layerTrans');
}

